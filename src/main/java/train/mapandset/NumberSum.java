  public int[] twoSum(int[] nums, int target) {

    Map<Integer, Integer> map = new HashMap<>();
    for (int i = 0; i < nums.length; i++) {
      map.put(nums[i], i);
    }

    for (int i = 0; i < nums.length; i++) {
      int el = target - nums[i];
      if (map.containsKey(el) && map.get(el) != i) {
        int x = map.get(el);
        return new int[] {i, x};
      }
    }

    throw new IllegalArgumentException(" no two sum");
  }

  public List<List<Integer>> threeSum(int[] nums) {

    Set<List<Integer>> result = new HashSet<>();

    if (nums == null || nums.length < 3) {
      return new ArrayList<>();
    }

    Arrays.sort(nums);

    Map<Integer, Integer> map = new HashMap<>();
    for (int i = 0; i < nums.length; i++) {
      map.put(nums[i], i);
    }

    if (map.size() == 1 && map.containsKey(Integer.valueOf(0))) {
      List<Integer> list = Arrays.asList(0, 0, 0);
      result.add(list);
      return new ArrayList<>(result);
    }

    int zero = 0;
    for (int i = 0; i < nums.length; i++) {
      int sub = zero - nums[i];
      for (int j = i + 1; j < nums.length; j++) {
        int el = sub - nums[j];
        if (map.containsKey(el) && map.get(el) != j && map.get(el) != i) {
          int x = map.get(el);
          List<Integer> list = Arrays.asList(nums[i], nums[j], nums[x]);
          Collections.sort(list);
          result.add(list);
        }
      }
    }

    return new ArrayList<>(result);
  }

  public List<List<Integer>> fourSum(int[] nums, int target) {
    Set<List<Integer>> result = new HashSet<>();

    if (nums == null || nums.length < 4) {
      return new ArrayList<>();
    }

    Arrays.sort(nums);

    Map<Integer, Integer> map = new HashMap<>();
    for (int i = 0; i < nums.length; i++) {
      map.put(nums[i], i);
    }

    if (map.size() == 1 && map.containsKey(Integer.valueOf(0))) {
      List<Integer> list = Arrays.asList(0, 0, 0);
      result.add(list);
      return new ArrayList<>(result);
    }

    for (int i = 0; i < nums.length; i++) {
      int sub0 = target - nums[i];
      for (int j = i + 1; j < nums.length; j++) {
        int sub1 = sub0 - nums[j];
        for (int x = j + 1; x < nums.length; x++) {
          int el = sub1 - nums[x];
          if (map.containsKey(el) && map.get(el) != j && map.get(el) != i && map.get(el) != x) {
            int v = map.get(el);
            List<Integer> list = Arrays.asList(nums[i], nums[j], nums[x], nums[v]);
            Collections.sort(list);
            result.add(list);
          }
        }
      }
    }

    return new ArrayList<>(result);
  }

